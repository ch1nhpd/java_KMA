package Buoi4.Bai12;

public class Nguoi {
	private String name;
	private int birthYear;
	private String code;
	public Nguoi() {
		super();
	}
	public Nguoi(String name, int birthYear, String code) {
		super();
		this.name = name;
		this.birthYear = birthYear;
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getBirthYear() {
		return birthYear;
	}
	public void setBirthYear(int birthYear) {
		this.birthYear = birthYear;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	
	

}
